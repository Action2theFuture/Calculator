def plus(a, b):
  return int(a) + int(b);

def minus(a, b):
  return int(a) - int(b);

def time(a, b):
  return int(a) * int(b);

def divisions(a, b):
  return int(a) / int(b);

def nagation(a):
  return -int(a);
def power(a, b):
  return int(a) ** int(b);

def reminder(a, b):
  return int(a) % int(b);

print(plus(a, b))
print(minus(a, b))
print(time(a, b))
print(divisions(a, b))
print(nagation(a))
print(power(a, b))
print(reminder(a, b))